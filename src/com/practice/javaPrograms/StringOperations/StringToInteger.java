package com.practice.javaPrograms.StringOperations;

public class StringToInteger {

	public static void main(String[] args) {
		String s="102";
		//converting given string to integer type and performing sum
		System.out.println(Integer.parseInt(s)+40);  //142
        System.out.println("3"+4); //34
        int n=8;
        System.out.println(n+1);  //9
        //converting integer to string
        System.out.println(Integer.toString(n)+1); //81
	}

}
